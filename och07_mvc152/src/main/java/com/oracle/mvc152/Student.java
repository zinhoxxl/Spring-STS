package com.oracle.mvc152;

public class Student {
	private String name;
	private int id;
	
	// getter / setter 
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	

}
